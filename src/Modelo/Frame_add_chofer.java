    private void btnAddChoferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddChoferActionPerformed
        // TODO add your handling code here:
        
        nombre = txtfNombreChofer.getText();
        edad = Integer.parseInt(txtfEdadChofer.getText());
        DNI = txtfDNIChofer.getText();
        tel = txtfTelChofer.getText();
        lic = txtfLicChofer.getText();
        if(Conductores.size() == 0){
            System.out.println("ID asignada: " + ID);
        }
        else{
            ID = Conductores.get(Conductores.size()-1).getIdConductor() + 1;
            System.out.println("ID asignada: " + ID);
        }
    
        //Guardamos el nuevo conductor en la lista
        Conductores.add(new Conductor(ID, lic, DNI, nombre, tel, edad));
        archivo.guardarObjetos("Conductores.txt", Conductores);
    }//GEN-LAST:event_btnAddChoferActionPerformed

